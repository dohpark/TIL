function solution(arr) {
  let h = arr.length;

  for (let i = h - 1; i > 0; i--) {
    for (let j = 0; j < arr[i - 1].length; j++) {
      arr[i - 1][j] += Math.max(arr[i][j], arr[i][j + 1]);
    }
  }
  return arr[0][0];
}

